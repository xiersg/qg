#格式化字符串
year = 2010
print('FishC成立与{}年'.format(year))
print("1+2={} 2的平方是{} 3的立方是{}".format(1+2,2**2,3**3))
#可以在花括号内指定索引值
print("{0}{0}{1}{1}".format('牛','魔'))
#还有关键字参数
#数字参数得在关键字参数之前
print("{le}{}".format("大牛魔",le = "x牛魔"))

#更多用法，详情请于鱼c搜 字符串格式化语法参考
#align < > = ^ 左右对其 数字类型对其 居中对齐
print("{:^10}".format("牛魔"))
print("{:<10}".format("牛魔"))
print("{:>10}".format("牛魔"))
# = 对齐方式只能是数字类型:使填充对象填充到符号之后数字之前
print("{:0=10}".format(-947))
#上为通式下为特式
print("{:=010}".format(-947))

